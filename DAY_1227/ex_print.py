# print() 함수에서 여러 개의 데이터를 분리해서 출력해주는 방법
# default : sep = " "(여러 변수 구분을 어떻게 할지)
# default : end = "\n"(출력 후 제일 끝에 추가할 문자)
# -----------------------------------------------------------
print()
print("홍", "길동", sep="^")
print("A",end="다음")
print("B",end="그리고")
print("C")

